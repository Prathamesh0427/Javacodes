public class Area {
    public static void main(String args[]) {
        int a, b, perimeter, area;
        a = 5;
        b = 2;
        area = a * b;
        perimeter = 2 * (a + b);
        System.out.println("Area of rectangle=" + area);
        System.out.println("Area of perimeter=" + perimeter);
    }

}
